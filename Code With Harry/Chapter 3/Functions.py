# len() Function
name = "Monty"
print(len(name))

# Endswith
print(name.endswith("ty"))

# Startswith
print(name.startswith("Mo"))

# Capitalize
NAME = name.capitalize()
print(NAME)

# Counts
print(name.count("t"))

# Upper
print(name.upper())

# Lower
print(name.lower())

# Title
print(name.title())

# Strip
print(name.strip())

# Find
s = " Hello World"
index = s.find("world") 
print(index)

# Replace
replaced = s.replace("world","python")
print(replaced)